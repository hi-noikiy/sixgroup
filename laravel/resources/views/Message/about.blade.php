        <h1>关于我们</h1>
        <p class="about_p">
        蜗牛（www.snail.com）是国内领先专业日租、短租房在线预订网站，于2012年8月正式上线，首轮获得了晨兴创投千万美元的A轮融资。目前在北京、上海等全国13个城市设有分公司，房源覆盖国内200多个城市。
        </p>
        <p class="about_p">
        作为Airbnb模式在中国的典型代表，蜗牛旨在为房东和房客搭建一个诚信、有保障的在线沟通和交易平台，并通过财产、人身安全保障方案及身份识别等机制建立绿色平台生态系统，有效的将房东的闲置资源通过分享充分利用并发挥最大价值，同时加强房东和房客间的社交关系及交互，为房客提供有别于传统酒店且更具自由的住宿体验。
        <br /><br /></p>
        <p class="about_p"><strong class="orange_1">对于房客：</strong></p>
        <p class="about_p">居住自由是蜗牛的特点，房源覆盖国内200多个城市，为旅游、求学、求职、就医、聚会、出差等出行人群提供更具性价比、更有家庭氛围、且更有居住自由的选择。<br />
        　　实地验真、实拍，确保房源的真实可靠。<br />
        　　当入住发生“预订房间无法入住”、“房间及设施与照片不符”、“房东临时提价”等情况时，“<a href="http://www.xiaozhu.com/supportplan" target="_blank">房客保障计划</a>”会为房客排忧解难，确保安心入住。<br /><br /></p>
        <p class="about_p"><strong class="orange_1">对于房东：</strong></p>
        <p class="about_p">蜗牛为有闲置房源、房间、沙发等的房东，提供了免费的分享推广平台，房东不用支付任何费用就可轻松发布房源信息。<br />
        　　<a href="http://www.xiaozhu.com/yunying.php?op=YunYing_LandlordInsurance" target="_blank">《家庭财产综合保险》</a>，为房东保驾护航，全面保障个人房东的财产安全。<br/>
        　　线上运营及线下管理团队，会为房东提供专业的服务，并定期邀请房东参加营销推广活动，确保房东获得收益。 <br /><br /></p>
